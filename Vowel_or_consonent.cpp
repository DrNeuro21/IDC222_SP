#include<iostream>
using namespace std;

int main()
{
    char x;
    cout << "Enter any Alphabet : " << endl;
    cin >> x;

    switch(x)
    {
        case 'a':
        case 'e':
        case 'i':
        case 'o':
        case 'u':
        case 'A':
        case 'E':
        case 'I':
        case 'O':
        case 'U':

            cout << "It is a vowel" << endl;
            break;
        
        default:
            cout << "Is a consonant" << endl;
    }
    return 0;
}